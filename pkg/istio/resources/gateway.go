package resources

import (
	istioapi "github.com/bevyx/istio-api-go/pkg/apis/networking/v1alpha3"
	api "github.com/bevyx/remesh/pkg/apis/remesh/v1alpha1"
	metav1 "k8s.io/apimachinery/pkg/apis/meta/v1"
	"k8s.io/apimachinery/pkg/runtime/schema"
)

//MakeIstioGateway is
func MakeIstioGateway(virtualApp api.VirtualApp, namespace string) (istioapi.Gateway, string) {
	istioServers := make([]istioapi.Server, 0)

	for _, server := range virtualApp.Spec.VirtualAppConfig.Servers {

		var tls *istioapi.Server_TLSOptions

		if server.Tls != nil {
			tls = &istioapi.Server_TLSOptions{
				HttpsRedirect:     server.Tls.HttpsRedirect,
				Mode:              istioapi.Server_TLSOptions_TLSmode(server.Tls.Mode),
				ServerCertificate: server.Tls.ServerCertificate,
				PrivateKey:        server.Tls.PrivateKey,
				CaCertificates:    server.Tls.CaCertificates,
				SubjectAltNames:   server.Tls.SubjectAltNames,
			}
		}

		istioServers = append(istioServers, istioapi.Server{
			Port: &istioapi.Port{
				Name:     server.Port.Name,
				Number:   server.Port.Number,
				Protocol: server.Port.Protocol,
			},
			Hosts: server.Hosts,
			Tls:   tls,
		})
	}
	gatewaySpec := istioapi.GatewaySpec{
		Selector: map[string]string{"istio": "ingressgateway"},
		Servers:  istioServers,
	}

	gatewayName := Prefix + virtualApp.Name + GatewaySuffix

	gateway := makeGateway(gatewayName, gatewaySpec, virtualApp)

	return gateway, gatewayName
}

func makeGateway(name string, gatewaySpec istioapi.GatewaySpec, virtualApp api.VirtualApp) istioapi.Gateway {
	return istioapi.Gateway{
		TypeMeta: metav1.TypeMeta{
			Kind:       "Gateway",
			APIVersion: "networking.istio.io/v1alpha3",
		},
		ObjectMeta: metav1.ObjectMeta{
			Name:      name,
			Namespace: virtualApp.Namespace,
			OwnerReferences: []metav1.OwnerReference{
				*metav1.NewControllerRef(&virtualApp, schema.GroupVersionKind{
					Group:   api.SchemeGroupVersion.Group,
					Version: api.SchemeGroupVersion.Version,
					Kind:    "VirtualApp",
				}),
			},
			Labels: AutoGeneratedLabels,
		},
		Spec: gatewaySpec,
	}
}
